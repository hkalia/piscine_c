#include "ft_h.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		ft_strlen(char *str)
{
	int len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

void	ft_putstr(char *str)
{
	int len;

	len = 0;
	while (str[len])
	{
		ft_putchar(str[len]);
		len++;
	}
}
