#include "ft_lib.h"

int		ft_atoi(char **str)
{
	int		nbr;
	int		is_neg;

	nbr = 0;
	if (**str == '-')
	{
		is_neg = 1;
		(*str)++;
	}
	while (**str >= '0' && **str <= '9')
	{
		nbr = nbr * 10 + (**str - '0');
		(*str)++;
	}
	if (is_neg)
		return (-nbr);
	else
		return (nbr);
}
